#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		nb;
	long	sign;

	i = 0;
	nb = 0;
	sign = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		sign++;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	if (sign == 1)
		return (-nb);
	return (nb);
}
